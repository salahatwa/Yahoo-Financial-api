package com.z2d.beans;

public class StockPrice {

	private String stockDate;
	private String stockOpen;
	private String stockClose;
	private String stockHigh;
	private String stockLow;
	private String stockVolume;
	private String stockAdjClose;
	private String currencey;

	public String getStockDate() {
		return stockDate;
	}

	public void setStockDate(String stockDate) {
		this.stockDate = stockDate;
	}

	public String getStockOpen() {
		return stockOpen;
	}

	public void setStockOpen(String stockOpen) {
		this.stockOpen = stockOpen;
	}

	public String getStockHigh() {
		return stockHigh;
	}

	public void setStockHigh(String stockHigh) {
		this.stockHigh = stockHigh;
	}

	public String getStockClose() {
		return stockClose;
	}

	public void setStockClose(String stockClose) {
		this.stockClose = stockClose;
	}

	public String getStockLow() {
		return stockLow;
	}

	public void setStockLow(String stockLow) {
		this.stockLow = stockLow;
	}

	public String getStockVolume() {
		return stockVolume;
	}

	public void setStockVolume(String stockVolume) {
		this.stockVolume = stockVolume;
	}

	public String getStockAdjClose() {
		return stockAdjClose;
	}

	public void setStockAdjClose(String stockAdjClose) {
		this.stockAdjClose = stockAdjClose;
	}

	public String getCurrencey() {
		return currencey;
	}

	public void setCurrencey(String currencey) {
		this.currencey = currencey;
	}
}
